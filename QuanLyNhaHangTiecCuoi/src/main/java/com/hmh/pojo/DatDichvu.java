public class DatDichvu {
    private int maDV;
    private String tenDV;
    private Long gia;

    /**
     * @return the maDV
     */
    public int getMaDV() {
        return maDV;
    }

    /**
     * @param maDV the maDV to set
     */
    public void setMaDV(int maDV) {
        this.maDV = maDV;
    }

    /**
     * @return the tenDV
     */
    public String getTenDV() {
        return tenDV;
    }

    /**
     * @param tenDV the tenDV to set
     */
    public void setTenDV(String tenDV) {
        this.tenDV = tenDV;
    }

    /**
     * @return the gia
     */
    public Long getGia() {
        return gia;
    }

    /**
     * @param gia the gia to set
     */
    public void setGia(Long gia) {
        this.gia = gia;
    }
}
