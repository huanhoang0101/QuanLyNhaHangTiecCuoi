public class DatMonan {
    private int maMenu;
    private String tenMon;
    private Long gia;
    private int soLuong;

    /**
     * @return the maMenu
     */
    public int getMaMenu() {
        return maMenu;
    }

    /**
     * @param maMenu the maMenu to set
     */
    public void setMaMenu(int maMenu) {
        this.maMenu = maMenu;
    }

    /**
     * @return the tenMon
     */
    public String getTenMon() {
        return tenMon;
    }

    /**
     * @param tenMon the tenMon to set
     */
    public void setTenMon(String tenMon) {
        this.tenMon = tenMon;
    }

    /**
     * @return the gia
     */
    public Long getGia() {
        return gia;
    }

    /**
     * @param gia the gia to set
     */
    public void setGia(Long gia) {
        this.gia = gia;
    }

    /**
     * @return the soLuong
     */
    public int getSoLuong() {
        return soLuong;
    }

    /**
     * @param soLuong the soLuong to set
     */
    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }
}
